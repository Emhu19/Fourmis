const int fourmi_height = sizeof(fourmi) / sizeof(fourmi[0]);

void afficher_fourmi(int position) {
    printf("\033[H\033[2J");

    for (int i = 0; i < HEIGHT; i++) {
        if (i < fourmi_height) {
            printf("%*s%s\n", position, "", fourmi[i]);
        } else {
            printf("\n");
        }
    }
}
